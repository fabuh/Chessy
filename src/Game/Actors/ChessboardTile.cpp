#include "pch.h"
#include "ChessboardTile.h"

namespace chessy
{
	//////////////////////////////////////////////////////////////////////////////////////////
	void ChessboardTile::Initialize(const EColor color)
	{
		m_color = color;
	}

	//////////////////////////////////////////////////////////////////////////////////////////
	const EColor ChessboardTile::GetColor() const
	{
		return m_color;
	}

	//////////////////////////////////////////////////////////////////////////////////////////
	void ChessboardTile::SetChessPiece(ChessPiece&& chessPiece)
	{
		m_chessPiece.emplace(std::move(chessPiece));
	}

	//////////////////////////////////////////////////////////////////////////////////////////
	void ChessboardTile::ClearChessPiece()
	{
		m_chessPiece.reset();
	}

	//////////////////////////////////////////////////////////////////////////////////////////
	const std::optional<ChessPiece>& ChessboardTile::GetChessPiece() const
	{
		return m_chessPiece;
	}

	//////////////////////////////////////////////////////////////////////////////////////////
	std::optional<ChessPiece>& ChessboardTile::GetMutableChessPiece()
	{
		return m_chessPiece;
	}
}